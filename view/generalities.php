        <h1>GBAF : Le Groupement Banque Assurance Français</h1>
    </diV>
    <p>Le Groupement Banque Assurance Français est une fédération représentant les 6 grands groupes français :</p>
        <ol>
            <li>BNP Paribas ;</li>
            <li>BPCE ;</li>
            <li>Crédit Agricole ;</li>
            <li>Crédit Mutuel-CIC ;</li>
            <li>Société Générale ;</li>
            <li>La Banque Postale.</li>
        </ol>
    <p>Même s'il existe une forte concurrence entre ces entités, elles vont toutes travailler de la même façon pour gérer près de 80 millions de comptes sur le territoire national.</p>
    <p>Le GBAF est le représentant de la profession bancaire et des assureurs sur tous les axes de la règlementation financière française. Sa mission est de promouvoir l'activité bancaire à l'échelle nationale. C'est aussi un interlocuteur privilégié des pouvoirs publics.</p>
